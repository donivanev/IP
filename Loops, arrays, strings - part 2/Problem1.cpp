#include <iostream>
using namespace std;

int main()
{
    int n, counter = 0;
    cout << "Enter n: ";
    cin >> n;
    int a[50];
    int b[50];

    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    for (int i = 0; i < n; i++)
    {
        cin >> b[i];
    }

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            if (a[i] == b[j])
            {
                counter++;
            }
        }
    }

    cout << "Equal elements: " << counter;

    return 0;
}